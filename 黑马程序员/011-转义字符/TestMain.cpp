#include <iostream>

using namespace std;

int main()
{
    // 换行符 \n 。
    cout << "Hello Clion C++ \nQsPlus。";

    // 反斜杠 \\ 。
    cout << "Hello Clion C++ \\QsPlus。" << endl;

    // 水平制表符 \t 。（水平制表符有对齐的效果）
    cout << "Hello Clion C++ \tQsPlus。" << endl;
    cout << "Hello Clion C++    \tQsPlus。" << endl;

    return 0;
}