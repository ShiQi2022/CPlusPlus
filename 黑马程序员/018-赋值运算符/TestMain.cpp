# include "iostream"

using namespace std;

int main()
{
    // =
    int a = 10;
    a = 100;
    cout << "a = " << a << endl;

    // +=
    int b = 10;
    b += 100;
    cout << "b = " << b << endl;

    // -=
    int c = 10;
    c -= 100;
    cout << "c = " << c << endl;

    // *=
    int d = 10;
    d *= 100;
    cout << "d = " << d << endl;

    // /=
    int e = 10;
    e /= 100;
    cout << "e = " << e << endl;

    // %=
    int f = 10;
    f %= 100;
    cout << "f = " << f << endl;

    return 0;
}