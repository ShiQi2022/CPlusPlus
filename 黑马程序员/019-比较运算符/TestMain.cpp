# include "iostream"

using namespace std;

int main()
{
    // ==
    cout << (1 == 2) << endl;

    // !=
    cout << (1 != 2) << endl;

    // >=
    cout << (1 >= 2) << endl;

    // <=
    cout << (1 <= 2) << endl;

    // >
    cout << (1 > 2) << endl;

    // <
    cout << (1 < 2) << endl;

    return 0;
}